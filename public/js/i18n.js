function translateDOMNode(node) {
  if (currentLang === 'zh') return;
  if (node.nodeType === Node.TEXT_NODE) {
    if (node.parentElement && node.parentElement.tagName === 'SCRIPT') return;
    if (node.parentElement && node.parentElement.tagName === 'STYLE') return;
    
    let text = node.nodeValue;
    let translated = translateText(text);
    if (translated !== text) {
      node.nodeValue = translated;
    }
  } else if (node.nodeType === Node.ELEMENT_NODE) {
    if (node.placeholder) node.placeholder = translateText(node.placeholder);
    if (node.title) node.title = translateText(node.title);
    if (node.alt) node.alt = translateText(node.alt);
    if (node.tagName === 'OPTION' && node.value && /[一-鿿]/.test(node.value)) {
      node.textContent = translateText(node.textContent);
    }
    node.childNodes.forEach(translateDOMNode);
  }
}

