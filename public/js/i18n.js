let currentLang = localStorage.getItem('lang') || 'zh';

function toggleLanguage() {
  currentLang = currentLang === 'zh' ? 'en' : 'zh';
  localStorage.setItem('lang', currentLang);
  location.reload();
}

function translateText(text) {
  if (currentLang === 'zh' || !text) return text;
  
  let trimmed = text.trim();
  if (translations.en[trimmed]) {
    return text.replace(trimmed, translations.en[trimmed]);
  }
  
  // Stem & Branch single characters
  const charMap = {
    '甲':'Jia','乙':'Yi','丙':'Bing','丁':'Ding','戊':'Wu','己':'Ji','庚':'Geng','辛':'Xin','壬':'Ren','癸':'Gui',
    '子':'Zi','丑':'Chou','寅':'Yin','卯':'Mao','辰':'Chen','巳':'Si','午':'Wu','未':'Wei','申':'Shen','酉':'You','戌':'Xu','亥':'Hai',
    '金':'Metal','木':'Wood','水':'Water','火':'Fire','土':'Earth',
    '月':'Month','年':'Year','日':'Day',
    '吉':'Good','凶':'Bad','平':'Fair',
    '今':'Today'
  };
  
  if (charMap[trimmed]) return text.replace(trimmed, charMap[trimmed]);
  
  // Replace sub-strings (e.g. "甲木")
  let translated = text;
  Object.keys(charMap).forEach(k => {
    translated = translated.replace(new RegExp(k, 'g'), charMap[k] + ' ');
  });
  
  // Cleanup extra spaces
  translated = translated.replace(/\s+/g, ' ').trim();
  
  return translated !== text ? translated : text;
}

function translateDOMNode(node) {
  if (currentLang === 'zh') return;
  if (node.nodeType === Node.TEXT_NODE) {
    if (node.parentElement && node.parentElement.tagName === 'SCRIPT') return;
    if (node.parentElement && node.parentElement.tagName === 'STYLE') return;
    
    let text = node.nodeValue;
    let translated = translateText(text);
    if (translated !== text) {
      node.nodeValue = translated;
    }
  } else if (node.nodeType === Node.ELEMENT_NODE) {
    if (node.tagName === 'INPUT' || node.tagName === 'SELECT' || node.tagName === 'BUTTON') {
      if (node.placeholder) node.placeholder = translateText(node.placeholder);
    }
    node.childNodes.forEach(translateDOMNode);
  }
}

document.addEventListener('DOMContentLoaded', () => {
  const btnLang = document.getElementById('btnLang');
  if (btnLang) {
    btnLang.innerHTML = currentLang === 'zh' ? '<i class="fas fa-globe mr-1"></i>EN' : '<i class="fas fa-globe mr-1"></i>中文';
  }
  
  if (currentLang === 'en') {
    translateDOMNode(document.body);
    
    // Use MutationObserver to catch dynamically added content (like calendar, analysis results)
    const observer = new MutationObserver((mutations) => {
      observer.disconnect(); // Prevent infinite loop during translation
      mutations.forEach(mutation => {
        if (mutation.type === 'childList') {
          mutation.addedNodes.forEach(translateDOMNode);
        } else if (mutation.type === 'characterData') {
          let text = mutation.target.nodeValue;
          let translated = translateText(text);
          if (translated !== text) mutation.target.nodeValue = translated;
        }
      });
      observer.observe(document.body, { childList: true, subtree: true, characterData: true });
    });
    
    observer.observe(document.body, { childList: true, subtree: true, characterData: true });
  }
});
