function getChangSheng(ganIdx, zhiIdx) {
  let start = CS_START[ganIdx];
  let isYang = ganIdx % 2 === 0;
  let offset;
  if (isYang) {
    offset = (zhiIdx - start + 12) % 12;
  } else {
    offset = (start - zhiIdx + 12) % 12;
  }
  return CS_NAMES[offset];
}
